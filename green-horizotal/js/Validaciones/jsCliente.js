function mostraCliente(dui, nombres, apellidos, direccion, telefono, bene1, bene2, bene3, fecha) {
    $("#duic").val(dui);
    $("#nombresc").val(nombres);
    $("#apellidosc").val(apellidos);
    $("#direccionc").val(direccion);
    $("#telefonoc").val(telefono);
    $("#bene1c").val(bene1);
    $("#bene2c").val(bene2);
    $("#bene3c").val(bene3);
    $("#fechac").val(fecha);
}
