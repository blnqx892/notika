function mostrarEmpleado(dui, nombres, apellidos, direccion, telefono, cargo, fecha) {
    $("#dui").val(dui);
    $("#nombres").val(nombres);
    $("#apellidos").val(apellidos);
    $("#direccion").val(direccion);
    $("#telefono").val(telefono);
    $("#cargo").val(cargo);
    $("#fech").val(fecha);
}